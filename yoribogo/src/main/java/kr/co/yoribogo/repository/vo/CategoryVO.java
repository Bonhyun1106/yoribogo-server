package kr.co.yoribogo.repository.vo;

public class CategoryVO {

	private String categoryNo;
	private String categoryName;
	private String categoryDistNo;
	private String categoryDistName;
	
	public String getCategoryNo() {
		return categoryNo;
	}
	public void setCategoryNo(String categoryNo) {
		this.categoryNo = categoryNo;
	}
	public String getCategoryName() {
		return categoryName;
	}
	public void setCategoryName(String categoryName) {
		this.categoryName = categoryName;
	}
	public String getCategoryDistNo() {
		return categoryDistNo;
	}
	public void setCategoryDistNo(String categoryDistNo) {
		this.categoryDistNo = categoryDistNo;
	}
	public String getCategoryDistName() {
		return categoryDistName;
	}
	public void setCategoryDistName(String categoryDistName) {
		this.categoryDistName = categoryDistName;
	}
	
}
